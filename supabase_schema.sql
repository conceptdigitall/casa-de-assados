-- Enable necessary extensions
create extension if not exists "uuid-ossp";

-- 1. PRODUCTS TABLE
create table public.products (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  description text,
  price numeric not null,
  category text not null,
  image_url text,
  stock integer default 0,
  min_stock integer default 5,
  barcode text unique,
  unit_type text default 'unit' check (unit_type in ('unit', 'kg'))
);

-- 2. COURIERS TABLE
create table public.couriers (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  active boolean default true
);

-- 3. DELIVERY ZONES TABLE
create table public.delivery_zones (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  price numeric not null
);

-- 4. ORDERS TABLE
create table public.orders (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  display_id text, -- stores the #123 format
  status text default 'Pendente',
  customer_name text,
  customer_phone text,
  customer_address text, -- JSON or text
  payment_method text,
  total numeric not null,
  delivery_fee numeric default 0,
  delivery_zone text,
  courier_id bigint references public.couriers(id),
  cancellation_reason text
);

-- 5. ORDER ITEMS TABLE
create table public.order_items (
  id bigint generated by default as identity primary key,
  order_id bigint references public.orders(id) on delete cascade not null,
  product_id bigint references public.products(id),
  product_name text, -- snapshot in case product is deleted
  quantity numeric not null,
  unit_price numeric not null,
  note text
);

-- 6. INVENTORY LOGS TABLE
create table public.inventory_logs (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  product_id bigint references public.products(id) on delete cascade not null,
  change_amount numeric not null,
  type text not null check (type in ('sale', 'manual_adjustment', 'restock')),
  reason text
);

-- 6. STORE SETTINGS TABLE (Single Row)
create table public.store_settings (
  id bigint generated by default as identity primary key,
  description text,
  phone text,
  instagram text,
  address text,
  weekday_hours text,
  weekend_hours text
);

