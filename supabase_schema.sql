-- POLICIES

-- Products: Everyone can view, only Admin (Auth) can edit
create policy "Public Products View" on public.products for select using (true);
create policy "Admin Products Edit" on public.products for all using (auth.role() = 'authenticated');

-- Couriers: Only Admin view/edit
create policy "Admin Couriers" on public.couriers for all using (auth.role() = 'authenticated');

-- Delivery Zones: Public view, Admin edit
create policy "Public Zones View" on public.delivery_zones for select using (true);
create policy "Admin Zones Edit" on public.delivery_zones for all using (auth.role() = 'authenticated');

-- Orders: Public can INSERT (Checkout). Only Admin can SELECT/UPDATE (Dashboard).
-- Ideally, customers should read their own orders via ID/Cookie logic, but for now assuming public cannot list all orders.
create policy "Public Create Order" on public.orders for insert with check (true); 
create policy "Admin Manage Orders" on public.orders for all using (auth.role() = 'authenticated');

-- Order Items: Same as orders
create policy "Public Create Order Items" on public.order_items for insert with check (true);
create policy "Admin Manage Order Items" on public.order_items for all using (auth.role() = 'authenticated');

-- Settings: Public view, Admin edit
create policy "Public Settings View" on public.store_settings for select using (true);
create policy "Admin Settings Edit" on public.store_settings for all using (auth.role() = 'authenticated');
